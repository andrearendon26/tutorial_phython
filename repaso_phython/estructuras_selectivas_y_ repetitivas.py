# Estructura Selectiva
# if-else

edad = 18
if edad >= 18:
    print("Eres Mayor de edad")
else:
    print("Eres menor de edad")

# if-elif-else

dia = "lunes"
if dia == "lunes":
    print("Hoy es lunes")
elif dia == "martes":
    print("Hoy es martes")
else:
    print("Hoy es otro dia de la semana")

